//standard input/output stream library
#include <iostream>

//starting of the main function
int main() {
    //initialsing an integer variable i with the value 1
    int i = 1;
    //while loop that runs as long as i is less than or equal to 10
    while (i <= 10) {
        //output the value of i followed by a space
        std::cout << i << " ";
        //increment the value of i by 1
        i++;
    }
    //successful program execution
    return 0;
}

